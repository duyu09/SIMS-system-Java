public class Student
{
    int StudentID;
    String Name;
    String Sex;
    String CardNum;
    String PhoneNum;
    String Location;

    public Student(int StudentID,String Name,String Sex,String CardNum,String Phone,String Location)
    {
        this.StudentID = StudentID;
        this.Name=Name;
        this.Sex=Sex;
        this.CardNum=CardNum;
        this.PhoneNum=Phone;
        this.Location=Location;
    }
}


